    <script type="text/javascript">
        var widthScreen = $(window).width();
        var heightScreen = $(window).height();
        var positionContent = (heightScreen / 2) - 130;

        function elementStyle(){
            if(widthScreen > 640){
                $('.content-section').css({'top': positionContent});
            } else {
                if(heightScreen > 480){
                    $('.content-section').css({'top': positionContent - 50});
                } else {
                    $('.content-section').css({'top': positionContent + 20});
                    $('.content-section .boxtext p').hide();
                }
            }
        }

        function elementLoad(){
            $('#wrapper').fullpage({
                verticalCentered: false,
                afterLoad: function(anchorLink, index){
                    if(index == 1){
                        $('#section-three .content-section').fadeOut();
                        $('#section-two .content-section').fadeOut();
                        $('#section-one .content-section').fadeIn();
                    }

                    if(index == 2){
                        $('#section-one .content-section').fadeOut();
                        $('#section-two .content-section').fadeIn();
                        $('#section-three .content-section').fadeOut();
                    }

                    if(index == 3){
                        $('#section-two .content-section').fadeOut();
                        $('#section-one .content-section').fadeOut();
                        $('#section-three .content-section').fadeIn();
                    }
                }
            });

            $('.toDown').click(function(e){
                e.preventDefault();
                $.fn.fullpage.moveSectionDown();
            });

            $('.toUp').click(function(e){
                e.preventDefault();
                $.fn.fullpage.moveSectionUp();
            });

            $('#section-one').backstretch([
                "<?php echo assets_url;?>img/landscape/Borobudur-Nothwest-view.jpg",
                ], {
                    fade: 750,
                    duration: 8000
            });

            $('#section-two').backstretch([
                "<?php echo assets_url;?>img/landscape/Danau Toba.jpg",
                ], {
                    fade: 750,
                    duration: 8000
            });

            $('#section-three').backstretch([
                "<?php echo assets_url;?>img/landscape/Gedung-Sate-Bandung-BY-Peter-van-Riel.jpg"
                ], {
                    fade: 750,
                    duration: 8000
            });

            setTimeout(function(){
                $('#section-one .content-section').fadeIn();
            }, 700);
        }

        $(document).ready(function(){
            elementStyle()
            elementLoad();
        });
    </script>

